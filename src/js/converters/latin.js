function convertLatin(aksaraText) {
  'use strict';

  const input = aksaraText;

  let output = input
    .replace(/h/g, '~h')
    .replace(/a/g, 'ha')
    .replace(/n/g, 'na')
    .replace(/c/g, 'ca')
    .replace(/r/g, 'ra')
    .replace(/k/g, 'ka')
    .replace(/d/g, 'dha')
    .replace(/f/g, 'da')
    .replace(/t/g, 'ta')
    .replace(/s/g, 'sa')
    .replace(/w/g, 'wa')
    .replace(/l/g, 'la')
    .replace(/p/g, 'pa')
    .replace(/j/g, 'ja')
    .replace(/y/g, 'ya')
    .replace(/v/g, 'nya')
    .replace(/m/g, 'ma')
    .replace(/g/g, 'ga')
    .replace(/b/g, 'ba')
    .replace(/q/g, 'tha')
    .replace(/z/g, 'nga')
    .replace(/!/g, 'na')
    .replace(/@/g, 'ka')
    .replace(/#/g, 'ta')
    .replace(/\$/g, 'sa')
    .replace(/%/g, 'pa')
    .replace(/\^/g, 'nya')
    .replace(/&/g, 'ga')
    .replace(/\*/g, 'ba')
    .replace(/ka\+/g, 'kha')
    .replace(/pa\+/g, 'fa')
    .replace(/da\+/g, 'dza')
    .replace(/ga\+/g, 'gha')
    .replace(/ja\+/g, 'za')
    .replace(/®/g, '#na')
    .replace(/¯/g, '#ka')
    .replace(/±/g, '#ta')
    .replace(/°/g, '#sa')
    .replace(/²/g, '#pa')
    .replace(/³/g, '#nya')
    .replace(/´/g, '#ga')
    .replace(/µ/g, '#ba')
    .replace(/H/g, '#ha')
    .replace(/N/g, '#na')
    .replace(/C/g, '#ca')
    .replace(/R/g, '#ra')
    .replace(/K/g, '#ka')
    .replace(/F/g, '#da')
    .replace(/T/g, '#ta')
    .replace(/S/g, '#sa')
    .replace(/W/g, '#wa')
    .replace(/L/g, '#la')
    .replace(/P/g, '#pa')
    .replace(/D/g, '#dha')
    .replace(/J/g, '#ja')
    .replace(/Y/g, '#ya')
    .replace(/V/g, '#nya')
    .replace(/M/g, '#ma')
    .replace(/G/g, '#ga')
    .replace(/B/g, '#ba')
    .replace(/Q/g, '#tha')
    .replace(/Z/g, '#nga')
    .replace(/#ka\+/g, '#kha')
    .replace(/#pa\+/g, '#fa')
    .replace(/#da\+/g, '#dza')
    .replace(/#ga\+/g, '#gha')
    .replace(/#ja\+/g, '#za')
    .replace(/i/g, '~i')
    .replace(/a~i/g, 'i')
    .replace(/u/g, '~u')
    .replace(/a~u/g, 'u')
    .replace(/e/g, '~e')
    .replace(/a~e/g, 'e')
    .replace(/\[/g, '~è')
    .replace(/~è(\w)a/g, '$1è')
    .replace(/~è(\w)(h|y|g)a/g, '$1$2è')
    .replace(/o/g, '~o')
    .replace(/(\w)è~o/g, '$1o')
    .replace(/\//g, '~r')
    .replace(/(\w+)(a|i|u|e|è|o)~r/g, '$1$2r')
    .replace(/(\w+)(a|i|u|e|è|o)~h/g, '$1$2h')
    .replace(/=/g, '~ng')
    .replace(/(\w+)(a|i|u|e|è|o)~ng/g, '$1$2ng')
    .replace(/\\/g, '~a')
    .replace(/(\w+)(a|i|u|e|è|o)~a/g, '$1')
    .replace(/-/g, '~ya')
    .replace(/(\w+)a~ya/g, '$1ya')
    .replace(/]/g, '~ra')
    .replace(/(\w+)a~ra/g, '$1ra')
    .replace(/\(/g, '~ru')
    .replace(/(\w+)a~ru/g, '$1ru')
    .replace(/\}/g, '~re')
    .replace(/(\w+)a~re/g, '$1re')
    .replace(/x/g, 're')
    .replace(/X/g, 'le')
    .replace(/;/g, '~b')
    .replace(/\?/g, '~c')
    .replace(/a#/g, '')
    .replace(/#(\w+)a\|/g, '$1u')
    .replace(/[~]\w+/g, '')
    .replace(/[~]è/g, '')
    .replace(/[#]\w+/g, '');

  return output;
}

export default convertLatin;
